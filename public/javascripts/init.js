$(function(){ 
    $(".cover").css({"opacity":0,top:"300px",height:"10px"});
    $(".person-img").hover(function(){
    	$(this).find(".cover")
    			.stop()
    			.animate({top:"10px",
						 "width":$(this).css("width"),
						 "height":$(this).css("height"),
						 "opacity":.8
						 },{queue:false,duration:160});
    },function(){
    	$(this).find(".cover")
    			.stop()
    			.animate({top:"10px",
						 "width":$(this).css("width"),
						 "height":$(this).css("height"),
						 "opacity":0
						 },{queue:false,duration:160});
    });
}); 